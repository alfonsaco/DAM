package Ejercicios;

public class Nivel_9_1 {
	int edad;
	String nombre;
	String apellidos;
	// Constructor
	public Nivel_9_1(int edad, String nombre, String apellidos) {
		this.edad = edad;
		this.nombre = nombre;
		this.apellidos = apellidos;
	}
	// Getters y Setters
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
}
