    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        String nombre = request.getParameter("nombre_pelicula");
        String genero = request.getParameter("genero_pelicula");
        // boolean estadoPelicula = request.getParameter("estado_pelicula");
        
        Pelicula p = new Pelicula();
        p.setNombre(nombre);
        p.setGenero(genero);
        
        if (PeliculaDao.registrar(p)) {
            request.setAttribute("mensaje", "El cine fue registrado");
        } else {
            request.setAttribute("mensaje", "El cine NO fue registrado");
        }
        
        request.getRequestDispatcher("registroCine.jsp").forward(request, response);
        
        
    }

