    // Métodos para verificar si los campos cumplen las condiciones
    private boolean esLetra(String palabra) {
        return palabra.matches("[a-zA-Z]+");
    }
    private boolean esDNI(String dni) {
        return dni.matches("[0-9]{8}[a-zA-Z]");
    }
    private boolean esEmail(String email) {
        return email.matches("[a-zA-Z0-9._%+-]+@[a-zA-Z0-9.-]+\\.[a-zA-Z]");
    }
    
    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        String nombre=jTextFieldNombre.getText();
        String apellidos=jTextFieldApellidos.getText();
        String DNI=jTextFieldDNI.getText();
        String email=jTextFieldEmail.getText();
        
        if(esLetra(nombre) && esLetra(apellidos) && esDNI(DNI) && esEmail(email)) {
            JOptionPane.showMessageDialog(null, "Usuario añadido con éxito");
            usuarios.add(new Usuario(nombre, apellidos, DNI, email));
            
        // Campos vacíos
        } else if(nombre.isEmpty() || apellidos.isEmpty() || DNI.isEmpty() || email.isEmpty()){
            JOptionPane.showMessageDialog(null, "No puede haber campos vacíos","Campos vacíos", JOptionPane.ERROR_MESSAGE);
            
        } else {
            // Alertas para cada dato del formulario inválido
            if(!esLetra(nombre)) {
                JOptionPane.showMessageDialog(null, nombre+" no puede contener números","Nombre inválido", JOptionPane.ERROR_MESSAGE);
            } else if(!esLetra(apellidos)) {
                JOptionPane.showMessageDialog(null, apellidos+" no puede contener números","Apellidos inválidos", JOptionPane.ERROR_MESSAGE);
            } else if(!esDNI(DNI)) {
                JOptionPane.showMessageDialog(null, DNI+" no es válido","DNI inválido", JOptionPane.ERROR_MESSAGE);
            } else if(!esEmail(email)) {
                JOptionPane.showMessageDialog(null, email+" no es válido","Email inválido", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jLabel1MouseClicked

