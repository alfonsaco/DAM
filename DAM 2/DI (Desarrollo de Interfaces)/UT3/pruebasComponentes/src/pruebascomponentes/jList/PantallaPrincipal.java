    private void jButtonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirActionPerformed
        String palabra=jTextField1.getText().toString();
        if(!palabra.isEmpty()) {
            // Añadir el ArrayList de palabras para actualizarlo
            modelo.addElement(palabra);
            jTextField1.setText("");
            
            // Si la palabra es "clear", se borrarán todos los elementos
            if(palabra.toLowerCase().equals("clear")) {
                modelo.removeAllElements();
            }
            
            if(modelo.contains("HOLA")) {
                System.out.println("La lista contiene la palabra HOLA");
            }
        }
    }//GEN-LAST:event_jButtonAnadirActionPerformed

