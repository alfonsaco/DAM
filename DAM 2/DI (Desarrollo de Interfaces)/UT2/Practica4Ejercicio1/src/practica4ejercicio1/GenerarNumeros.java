    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed
        int numeroRandom=0;
        
        if(jRadioButton0a100.isSelected()) {
            numeroRandom=(int) Math.round(1+Math.random()*99);
        } else if(jRadioButton100a200.isSelected()) {
            numeroRandom=(int) Math.round(100+Math.random()*100);
        } else if(jRadioButton200a500.isSelected()) {
            numeroRandom=(int) Math.round(200+Math.random()*300);
        } else {
               numeroRandom=0; 
        }
        
        jLabelResultado.setText(Integer.toString(numeroRandom));
    }//GEN-LAST:event_jButtonGenerarActionPerformed

